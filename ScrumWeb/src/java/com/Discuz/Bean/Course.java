package com.Discuz.Bean;

/**
 *
 * @author OovEver
 */
public class Course {
    private int id;
    private String School;
    private String Academy;
    private String Courese;
    private String Img;
  

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the School
     */
    public String getSchool() {
        return School;
    }

    /**
     * @param School the School to set
     */
    public void setSchool(String School) {
        this.School = School;
    }

    /**
     * @return the Academy
     */
    public String getAcademy() {
        return Academy;
    }

    /**
     * @param Academy the Academy to set
     */
    public void setAcademy(School Academy) {
        this.setAcademy(Academy);
    }

    /**
     * @return the Courese
     */
    public String getCourese() {
        return Courese;
    }

    /**
     * @param Courese the Courese to set
     */
    public void setCourese(String Courese) {
        this.Courese = Courese;
    }

    /**
     * @return the Img
     */
    public String getImg() {
        return Img;
    }

    /**
     * @param Img the Img to set
     */
    public void setImg(String Img) {
        this.Img = Img;
    }

    /**
     * @param Academy the Academy to set
     */
    public void setAcademy(String Academy) {
        this.Academy = Academy;
    }
    
    
}
